export default async function handler(req, res) {
  if (req.method !== 'POST') {
    res.setHeader('Content-Type', 'application/json');
    return res.status(405).json({ error: 'Method not allowed' });
  }

  try {
    const { message, conversationHistory = [] } = req.body;

    // 调用智谱 API（流式）
    const response = await fetch(ZHIPU_API_URL, {
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
        'Authorization': `Bearer ${ZHIPU_API_KEY}`
      },
      body: JSON.stringify({
        model: ZHIPU_MODEL,
        messages: [
          { role: 'system', content: KELLY_SYSTEM_PROMPT },
          ...conversationHistory,
          { role: 'user', content: message }
        ],
        max_tokens: 2000,
        stream: true,
        thinking: false
      })
    });

    if (!response.ok) {
      const errorText = await response.text();
      console.error('智谱 API 错误:', errorText);
      res.setHeader('Content-Type', 'application/json');
      return res.status(500).json({ success: false, error: `API 调用失败: ${response.status}` });
    }

    // 转发 SSE 流给前端
    res.setHeader('Content-Type', 'text/event-stream');
    res.setHeader('Cache-Control', 'no-cache');
    res.setHeader('Connection', 'keep-alive');

    const reader = response.body.getReader();
    const decoder = new TextDecoder();

    while (true) {
      const { done, value } = await reader.read();
      if (done) break;

      const chunk = decoder.decode(value, { stream: true });
      const lines = chunk.split('\n');

      for (const line of lines) {
        if (!line.startsWith('data: ')) continue;
        const data = line.slice(6);
        if (data === '[DONE]') continue;

        try {
          const parsed = JSON.parse(data);
          const delta = parsed.choices?.[0]?.delta;

          // 推理内容（思考过程）
          if (delta?.reasoning_content) {
            res.write(`data: ${JSON.stringify({ reasoning: delta.reasoning_content })}\n\n`);
          }
          // 正常回复内容
          if (delta?.content) {
            res.write(`data: ${JSON.stringify({ text: delta.content })}\n\n`);
          }
        } catch {}
      }
    }

    res.write('data: [DONE]\n\n');
    res.end();

  } catch (error) {
    console.error('对话 API 错误:', error);
    if (!res.headersSent) {
      res.setHeader('Content-Type', 'application/json');
      res.status(500).json({ success: false, error: error.message || '未知错误' });
    }
  }
}
